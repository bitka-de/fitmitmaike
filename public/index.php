  function e(string $value): string
  {
    return htmlspecialchars($value, ENT_QUOTES, 'UTF-8');
  }

  function loadNewsPosts(): array
  {
    $dataDir = __DIR__ . '/inside/news_data';
    $posts = [];

    if (!is_dir($dataDir)) {
      return [];
    }

    $files = glob($dataDir . '/*.json') ?: [];

    foreach ($files as $file) {
      $content = file_get_contents($file);
      if ($content === false) {
        continue;
      }

      $data = json_decode($content, true);
      if (!is_array($data)) {
        continue;
      }

      if (empty($data['headline']) || empty($data['slug'])) {
        continue;
      }

      $posts[] = $data;
    }

    usort($posts, static function (array $a, array $b): int {
      $dateA = strtotime($a['created_at'] ?? '1970-01-01 00:00:00');
      $dateB = strtotime($b['created_at'] ?? '1970-01-01 00:00:00');
      return $dateB <=> $dateA;
    });

    return $posts;
  }

  function buildImageUrl(string $relativePath): string
  {
    $relativePath = str_replace('\\', '/', $relativePath);
    $parts = explode('/', $relativePath);
    $parts = array_map('rawurlencode', $parts);
    return 'inside/uploads/' . implode('/', $parts);
  }

  function formatNewsDate(string $date): string
  {
    if (trim($date) === '') {
      return '';
    }

    $timestamp = strtotime($date);
    if ($timestamp === false) {
      return '';
    }

    $months = [
      1 => 'Januar',
      2 => 'Februar',
      3 => 'März',
      4 => 'April',
      5 => 'Mai',
      6 => 'Juni',
      7 => 'Juli',
      8 => 'August',
      9 => 'September',
      10 => 'Oktober',
      11 => 'November',
      12 => 'Dezember',
    ];

    $day = date('j', $timestamp);
    $month = $months[(int) date('n', $timestamp)] ?? '';
    $year = date('Y', $timestamp);

    return $day . '. ' . $month . ' ' . $year;
  }

